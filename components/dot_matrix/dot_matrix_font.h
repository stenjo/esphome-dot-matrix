// ---------------------------------------------------------------------------
//  Font data ported verbatim from the original DotMatrix MicroPython module
//  (cmodules/DotMatrix/src/matrix.c).
//
//  Encoding scheme (unchanged):
//    * Each glyph is stored as: [width][col0][col1]...[col(width-1)]
//      so character widths are flexible (1..6 columns).
//    * ASCII 32..126 sit at their natural positions.
//    * Norwegian letters are placed at the byte values that match the *second*
//      byte of their UTF-8 encoding, and 0xC3 (195) is used as an escape:
//          Å 0xC3 0x85 (133)   Æ 0xC3 0x86 (134)   Ø 0xC3 0x98 (152)
//          å 0xC3 0xA5 (165)   æ 0xC3 0xA6 (166)   ø 0xC3 0xB8 (184)
//      The degree sign ° is placed at byte 176 and reached via the 0xC2
//      escape (UTF-8 0xC2 0xB0), so "21.5°C" renders directly.
//      Further 0xC2 symbols live at their continuation-byte positions:
//      « 0xAB(171)  ± 0xB1(177)  ² 0xB2(178)  ³ 0xB3(179)  µ 0xB5(181)
//      » 0xBB(187, FONT_INDEX extended to 156 entries).
//      This means plain UTF-8 text ("Blåbær", "Mandag") renders correctly
//      with no transcoding — the bytes flow straight through.
// ---------------------------------------------------------------------------

